static float digits_X[1797 * 64];
static float digits_y[1797];
static int digits_initialized = 0;

static void init_digits(void) {
    if (digits_initialized) return;
    digits_initialized = 1;

    uint32_t rng = 12345u;
    int idx = 0;

    for (int d = 0; d < 10; d++) {
        /* 7*180 + 3*179 = 1260 + 537 = 1797 */
        int count = (d < 7) ? 180 : 179;

        for (int s = 0; s < count; s++) {
            for (int p = 0; p < 64; p++) {
                /* LCG step */
                rng = rng * 1103515245u + 12345u;
                float noise = ((float)((rng >> 16) & 0x7FFF) / 32767.0f - 0.5f) * 3.0f;
                float val = digit_templates[d][p] + noise;
                if (val < 0.0f)  val = 0.0f;
                if (val > 16.0f) val = 16.0f;
                digits_X[idx * 64 + p] = val;
            }
            digits_y[idx] = (float)d;
            idx++;
        }
    }
}

const float* cml_builtin_digits_data(int* n, int* features) {
    init_digits();
    *n = 1797;
    *features = 64;
    return digits_X;
}

const float* cml_builtin_digits_labels(int* n) {
    init_digits();
    *n = 1797;
    return digits_y;
}
